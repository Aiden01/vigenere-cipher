use nalgebra::DMatrix;

type IMatrix = nalgebra::Matrix<
    char,
    nalgebra::Dynamic,
    nalgebra::Dynamic,
    nalgebra::VecStorage<char, nalgebra::Dynamic, nalgebra::Dynamic>,
>;

pub struct Vigenere {
    matrix: IMatrix,
}

impl Vigenere {
    pub fn new() -> Vigenere {
        Vigenere {
            matrix: create_matrix(),
        }
    }

    /**
     * Encrypts a plain text with a key
     */
    pub fn encrypt<T>(&self, plain: T, key: T) -> String
    where
        T: Into<String>,
    {
        let text: String = plain.into();
        let mut k: String = key.into();
        let mut result = String::new();

        if k.len() != text.len() {
            k = self.set_key_len(&mut k, text.len())
        }

        let mut i = 0;
        for c in text.chars() {
            i += 1;
            let key_char = k.chars().nth(i - 1).unwrap();
            result.push(self.get_matched_char(key_char, c));
        }

        result
    }

    /**
     * Sets the len of the key according to the len of the plain text
     */
    fn set_key_len(&self, k: &mut String, len: usize) -> String {
        while k.len() < len {
            k.push_str(&k.clone());
        }
        let temp = &k[0..len];
        String::from(temp)
    }

    fn get_char_num(&self, c: char) -> usize {
        let alphabet = String::from("abcdefghijklmnopqrstuvwxyz");
        alphabet.find(c).unwrap()
    }

    /**
     * Returns the matched char on the matrix
     */
    fn get_matched_char(&self, k: char, c: char) -> char {
        self.matrix.row(self.get_char_num(c))[self.get_char_num(k)]
    }
}

